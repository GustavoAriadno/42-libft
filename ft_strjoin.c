#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char			*str;

	if (!s1 || !(str = (char *)malloc(ft_strlen(s1) + ft_strlen(s2) + 1)))
		return (NULL);
	ft_memcpy(str, s1, ft_strlen(s1));
	ft_memcpy(str + ft_strlen(s1), s2, ft_strlen(s2));
	str[ft_strlen(s2) + ft_strlen(s1)] = '\0';
	return (str);
}
